package segment;

/**
 * Interface für ein Segment - dieses enthält in jedem Fall eine Anfangs- und Endposition
 * @author bublm1
 */
public interface Segment {

	/**
	 * @author bublm1
	 * @return
	 */
	public abstract int start();

	/**
	 * @author bublm1
	 * @return
	 */
	public abstract int end();

}